# 雷达探测范围曲面数据
def create_surface3d_data(a, b, c=0, R=150000.0, step=10):
    surface3d_data = []
    for x in range(a - R, a + R, step):
        for y in range(b - R, b + R, step):
            t = R ** 2 - (x - a) ** 2 - (y - b) ** 2
            if t >= 0:
                z = c + t ** .5
                surface3d_data.append([x, y, z])
    return surface3d_data
